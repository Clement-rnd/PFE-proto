export interface Country {
  name: string;
  dialCode: string;
  code: string;
}

function flag(code: string): string {
  return code
    .toUpperCase()
    .split('')
    .map(c => String.fromCodePoint(127397 + c.charCodeAt(0)))
    .join('');
}

